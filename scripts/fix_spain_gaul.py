print(f'Всего исправлений: {len(CORRECTIONS)}')

with open('data/region_assignments.js', 'r') as f:
    text = f.read()

changes = 0
old_nations = {}
for region_id, new_nation in CORRECTIONS.items():
    m = re.search(r'"' + region_id + r'"\s*:\s*"([^"]*)"', text)
    old = m.group(1) if m else '?'
    old_nations[region_id] = old
    new_text = re.sub(
        r'("' + region_id + r'"\s*:\s*)"[^"]*"',
        r'\1"' + new_nation + '"',
        text
    )
    if new_text != text:
        changes += 1
    text = new_text

with open('data/region_assignments.js', 'w') as f:
    f.write(text)

print(f'Изменено {changes} регионов')
from collections import Counter
old_c = Counter(old_nations.values())
new_c = Counter(CORRECTIONS.values())
print('\nБыло:')
for n, c in old_c.most_common():
    print(f'  {n:<25} {c}')
print('\nСтало:')
for n, c in new_c.most_common():
    print(f'  {n:<25} {c}')

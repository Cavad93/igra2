changes = 0
for region_id, new_nation in CORRECTIONS.items():
    # Узнаём старое значение
    m = re.search(r'"' + region_id + r'"\s*:\s*"([^"]*)"', text)
    old = m.group(1) if m else '?'
    old_nations[region_id] = old

    new_text = re.sub(
        r'("' + region_id + r'"\s*:\s*)"[^"]*"',
        r'\1"' + new_nation + '"',
        text
    )
    if new_text != text:
        changes += 1
    text = new_text

with open('data/region_assignments.js', 'w') as f:
    f.write(text)

print(f'Изменено {changes} регионов\n')

# Статистика

#!/usr/bin/env python3
"""
Исправление нейтральных регионов с историческими/географическими названиями.

Регионы с ИСТОРИЧЕСКИМИ названиями (греческие топонимы):
─────────────────────────────────────────────────────
  Kamarina (r2403, lat 36.85, lon 14.70) — Камарина, СВ Сицилия
    Древнегреческий город, разрушен Агафоклом в 309 BC.
    В 304 BC — руины в пределах Сиракузской сферы влияния. → syracuse

  Meligounis (r3199, lat 38.56, lon 14.83) — Мелигунис, Липарские острова
    Древнее название острова Липари (Эолийские острова).
    В 304 BC захвачены Агафоклом обманом (50 талантов контрибуции). → syracuse

Регионы с ГЕОГРАФИЧЕСКИМИ названиями в Италии (нейтральные анклавы):
─────────────────────────────────────────────────────────────────────
  Апулия 16 (r2149, lat 40.98, lon 15.22) — Южная Апулия / Хирпиния
    Окружён лукансими (восток/юг) и Римом (запад/север). Область племени
    хирпинов (ветвь самнитов). Ближайшая нация в игре — lucani. → lucani

  Лигурия 2 (r2220, lat 44.44, lon 9.99) — Лигурийские Апеннины
    Между апуанами (юг, 44.22/9.86) и Лаэвами (север, 44.93/9.99).
    Апуаны (Apuani Ligures) — горное племя С.Тосканы/Ю.Лигурии. → apuania

  Цизальпийская_Галлия 36 (r2241, lat 45.69, lon 12.75) — Тревизо/Беллуно
    Территория италийских Венетов (не кельтов), союзников Рима.
    Нация «veneti» в игре соответствует Венетам Бретани (Галлия).
    Ближайшая нация в игре — carni (45.56, 13.71). → carni
"""
import re, json

CORRECTIONS = {
    # ══ СИЦИЛИЯ — исторические топонимы ═══════════════════════════
    # Камарина (Camarina) — греч. колония в ЮВ Сицилии, основана 598 BC,
    # неоднократно разрушалась. Разрушена Агафоклом в 309 BC как союзник
    # Карфагена. В 304 BC — территория Сиракузского государства.
    'r2403': 'syracuse',  # 36.85, 14.70 — Камарина (Camarina)

    # Мелигунис (Meligounis) — древнее название Липари (Liparos/Lipara).
    # В 304 BC именно в этом году Агафокл Сиракузский захватил острова
    # обманом, обложив жителей данью 50 талантов. → syracuse
    'r3199': 'syracuse',  # 38.56, 14.83 — Мелигунис/Липари (Aeolian Is.)

    # ══ ИТАЛИЯ — географические анклавы ════════════════════════════
    # Апулия 16 (Хирпиния): в 304 BC — территория хирпинов (Hirpini),
    # самнитской субгруппы. Нет нации hirpini → ближайший вариант lucani.
    'r2149': 'lucani',    # 40.98, 15.22 — Хирпиния (Апулия 16)

    # Лигурия 2: Лигурийские Апеннины между Генуэзским заливом и долиной По.
    # Апуаны (Apuani Ligures) жили именно здесь — Тоскана-Лигурия граница.
    'r2220': 'apuania',   # 44.44,  9.99 — Апуанские Апеннины (Лигурия 2)

    # Цизальпийская Галлия 36: Предгорья Беллуно/Тревизо — территория
    # италийских Венетов. В игре нет нации для Венетов Северной Италии
    # (venetii = Венеты Бретани). Ближайшие: карны (45.56, 13.71). → carni
    'r2241': 'carni',     # 45.69, 12.75 — Беллуно/Тревизо (Венетия)
}

print(f'Всего исправлений: {len(CORRECTIONS)}')

with open('data/region_assignments.js', 'r') as f:
    text = f.read()

changes = 0
old_nations = {}
for region_id, new_nation in CORRECTIONS.items():
    m = re.search(r'"' + region_id + r'"\s*:\s*"([^"]*)"', text)
    old = m.group(1) if m else '?'
    old_nations[region_id] = old
    new_text = re.sub(
        r'("' + region_id + r'"\s*:\s*)"[^"]*"',
        r'\1"' + new_nation + '"',
        text
    )
    if new_text != text:
        changes += 1
    text = new_text

with open('data/region_assignments.js', 'w') as f:
    f.write(text)

print(f'Изменено {changes} регионов')
print()
for rid, new_nation in CORRECTIONS.items():
    old = old_nations.get(rid, '?')
    print(f'  {rid:8} {old:<20} → {new_nation}')
